package co.xiaodao.weixin.menu;

public class CommonButton extends Button {

	private String type;
	private String key;

	public CommonButton() {

	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}
